int	ft_strcmp(char *s1, char *s2)
{
	int res;
	int i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
	{
		i++;
		if (s1[i + 1] == '\0' && s2[i + 1] == '\0')
			res = 0;
	}
	if (s1[i] != s2[i])
		res = s1[i] - s2[i];
	return (res);
}
